	.ORG $0200

RESTART:
	LDA #$00
	STA $F9
	STA $FB
	LDA #$EF
	STA $FA
	LDA #00

MAIN:
	JSR $1F1F  ;SCANDS
	JSR $1F6A  ;GETKEY
	CMP #$0E
	BNE CONT
	JMP $1C64
CONT:
	CMP #$07
	BEQ INVIS
	CMP #$06
	BEQ DEVIS
	CMP #$03
	BEQ INHM
	CMP #$02
	BEQ DEHM
	CMP #$00
	BEQ RESTART
	JMP MAIN

INVIS:
	CLC
	SED
	LDA #$01
	ADC $FB
	STA $FB
	CLD
	JMP MAIN

DEVIS: 
	SED
	LDA $FB
	SEC
	SBC #$01
	STA $FB
	CLC
	CLD
	JMP MAIN

INHM:
	CLC
	SED
	LDA #$01
	ADC $F9
	STA $F9
	CLD
	JMP MAIN

DEHM: 
	SED
	LDA $F9
	SEC
	SBC #$01
	STA $F9
	CLC
	CLD
	JMP MAIN

